const sleep = (ms: number) => new Promise(resolve => setTimeout(resolve, ms))

async function upsertWithRetry(distName: string, govId: number, retries = 3): Promise<any> {
  for (let attempt = 1; attempt <= retries; attempt++) {
    try {
      const res = await prisma.district.upsert({
        where: {
          nameAr_governorateId: {
            nameAr: distName,
            governorateId: govId
          }
        },
        update: {},
        create: {
          nameAr: distName,
          governorateId: govId
        }
      })
      return res
    } catch (error) {
      if (attempt === retries) {
        throw error
      }
      console.warn(`⚠️ فشل المحاولة ${attempt} لإضافة ${distName}. إعادة المحاولة بعد ثانية...`)
      await sleep(1000)
    }
  }
}

async function main() {
  console.log("⏳ البدء في ربط وإدخال المديريات...")

  for (const [govName, districts] of Object.entries(districtsData)) {
    // Get governorate
    const gov = await prisma.governorate.findUnique({
      where: { nameAr: govName }
    })

    if (!gov) {
      console.warn(`⚠️ لم يتم العثور على محافظة: ${govName}`)
      continue
    }

    console.log(`\n🗺️ جاري معالجة مديريات محافظة: ${govName}...`)

    for (const distName of districts) {
      const res = await upsertWithRetry(distName, gov.id)
      console.log(`  ✅ تم ربط المديرية: ${res.nameAr}`)
      await sleep(50) // 50ms delay between calls
    }
  }

  console.log("\n🎉 تم الانتهاء من إدخال كافة المديريات بنجاح!")
}

main()
  .catch((e) => {
    console.error("❌ حدث خطأ أثناء إضافة المديريات:", e)
    process.exit(1)
  })
  .finally(async () => {
    await prisma.$disconnect()
  })
